class CircularQueue
  def initialize(spots)
    @circle = Array.new(spots, nil)
  end

  def enqueue(value)
    @circle[0] = value
    @circle.rotate!
  end

  def dequeue
    to_remove = @circle.reject(&:nil?).first
    @circle[@circle.index(to_remove)] = nil
    to_remove
  end
end

queue = CircularQueue.new(3)
puts queue.dequeue == nil

queue.enqueue(1)
queue.enqueue(2)
puts queue.dequeue == 1

queue.enqueue(3)
queue.enqueue(4)
puts queue.dequeue == 2

queue.enqueue(5)
queue.enqueue(6)
queue.enqueue(7)
puts queue.dequeue == 5
puts queue.dequeue == 6
puts queue.dequeue == 7
puts queue.dequeue == nil

